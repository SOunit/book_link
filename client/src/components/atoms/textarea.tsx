import { FC } from 'react';
import classes from './textarea.module.css';

type Props = {
  onChange: any;
  value?: string;
  className?: string;
  placeholder?: string;
};

export const Textarea: FC<Props> = ({
  onChange,
  value,
  className,
  placeholder,
}) => {
  return (
    <textarea
      onChange={onChange}
      value={value}
      className={`${classes['textarea']} ${className}`}
      placeholder={placeholder}
    />
  );
};
